    private void jButton_ActualizarEstatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarEstatusActionPerformed

        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "update gobernadores set Estatus = ? where Ciudad = '"+ciudad+"'");

            pst.setInt(1, Integer.parseInt(txt_Nuevo_Estatus.getText()));
            pst.executeUpdate();
            cn.close();

        } catch (SQLException e) {
            System.err.println("Error al actualizar nuevo Estatus desde mapa: "+e);
        }
        
        this.dispose();

    }//GEN-LAST:event_jButton_ActualizarEstatusActionPerformed

