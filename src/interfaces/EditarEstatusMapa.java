    public EditarEstatusMapa() {
        initComponents();
        setSize(300,150);
        setResizable(false);
        setLocationRelativeTo(null);
        setTitle("Editar " + ciudad);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        
        txt_Ciudad.setText(ciudad);
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "select * from gobernadores where Ciudad = '"+ ciudad +"'");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                txt_Nuevo_Estatus.setText(rs.getString("Estatus"));
            }
            cn.close();

        } catch (SQLException e) {
            System.err.println("Error al tomar estatus: "+e);
        }
    }
    
